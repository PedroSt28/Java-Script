/*
Tipos de dados nativos do JS

number: Para números inteiros ou decimais, positivos ou negativos. Ex: 10, -3.14
string: Para qualquer texto que seja necessário. Ex: nomes, senhas, emails, telefones
boolean: Tipo lógico, podendo apenas ser true ou false.
*/

// number

console.log(10, typeof(10))
console.log(-10, typeof(-10))
console.log(3.14, typeof(3.14))
console.log(10_000_000, typeof(10_000_000))
console.log()

// string
console.log('olá mundo', typeof('olá mundo'))
console.log("olá mundo", typeof("olá mundo"))
console.log('senha123', typeof('senha123'))
console.log('11999999999', typeof('11999999999'))
console.log()

// boolean
console.log(true, typeof(true))
console.log(false, typeof(false))
